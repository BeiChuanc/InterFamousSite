import Foundation

/**
 
 ProgressHUD
 des：用于在 iOS 应用中显示简单的加载提示框、进度指示器等，给用户直观的操作反馈，告知当前任务的执行状态，比如正在加载网络数据、保存文件等操作时显示等待提示。
 
 SnapKit
 des：一个强大的自动布局（Auto Layout）框架，使用简洁的代码来创建和管理视图的布局约束，相比原生的布局代码更加直观、易读。
 
 'AgoraRtcEngine_iOS', '~> 3.7.1''
 des：声网（Agora）提供的实时音视频通信引擎，用于在 iOS 应用中实现音视频通话、直播互动、实时互动课堂等功能，能方便地进行音视频的采集、编码、传输、解码和播放等一系列操作。
 
 'Alamofire', '~> 4.9.1'
 des：一款优雅的网络请求框架，简化了 iOS 中进行 HTTP 网络请求（如 GET、POST 等）的操作，支持请求和响应的序列化、验证、缓存等功能，让网络通信代码更加简洁高效。
 
 'SwiftyJSON', '~> 4.0'
 des：方便地处理 JSON 数据，让 JSON 数据的解析、取值、修改等操作更加简洁直观，相比直接使用原生的 JSON 解析方式代码可读性更强。
 
 'QKeyboardManagerSwift
 des：用于自动管理 iOS 应用中的键盘交互，例如当键盘弹出时，自动调整视图布局避免键盘遮挡输入框，还能方便地处理键盘的显示隐藏动画等相关操作，提升用户输入体验。
 
 'KTVHTTPCache', '~> 3.0.0'
 des：主要用于对 iOS 应用中的 HTTP 网络请求进行缓存管理，可缓存网络资源（如图片、脚本文件等），下次请求相同资源时能直接从缓存中读取，提高应用性能，减少网络流量消耗，同时支持缓存的过期策略等配置。
 
 NVActivityIndicatorView
 des：提供各种样式的加载指示器（Activity Indicator），可以自定义指示器的颜色、大小、样式等，用于在应用执行耗时操作时给用户展示正在加载的视觉提示。
 
 'Kingfisher', '7.6.2'
 des：一个轻量级的图片加载和缓存框架，能方便地从网络、本地等多种来源加载图片并展示到 UIImageView 上，同时自动处理图片的缓存，提高图片加载效率，减少重复下载。
 
 'Starscream', '4.0.4'
 des：用于在 iOS 应用中实现 WebSocket 通信，方便与支持 WebSocket 协议的服务器进行实时双向通信，常用于即时通讯、实时数据推送等场景。
 
 DropDown
 des：用于创建下拉菜单，方便在 iOS 界面中提供选项列表供用户选择，可自定义下拉菜单的样式、显示位置、数据源等。
 
 'Moya', '~> 13.0.1'
 des：是对 Alamofire 等网络请求库的进一步封装，提供了更加清晰、面向协议的网络请求抽象层，方便进行网络请求的配置、管理以及对不同业务接口请求的分类组织，使网络请求相关代码结构更清晰。
 
 HandyJSON
 des：用于将 JSON 数据快速、方便地转换为 Swift 中的模型对象，反之亦然，通过简单的协议实现，简化了 JSON 数据和模型之间的序列化和反序列化过程，提高开发效率。
 
 'CollectionViewPagingLayout'
 des：是针对 UICollectionView 的一种自定义布局方式，常用于实现类似分页滑动的效果，比如水平或垂直方向的分页滚动展示图片、卡片等内容，提供了丰富的布局配置选项来满足不同的页面展示需求。
 
 'lottie-ios', '~> 3.4.3'
 des：用于在 iOS 应用中轻松集成和展示动画，支持播放 Adobe After Effects 通过 Bodymovin 插件导出的 JSON 格式动画文件，可用于制作炫酷的加载动画、引导页动画等。
 
 GSMessages
 des：用于在 iOS 应用中方便地显示各种样式的提示消息，如成功提示、错误提示、警告提示等，消息可以自动弹出、自动消失，并且可以自定义显示位置、样式、动画效果等，提升用户交互反馈效果。
 
 AppsFlyerFramework
 des：主要用于移动应用的营销归因分析，帮助开发者追踪应用的安装来源、用户行为、广告效果等数据，以便更好地了解用户获取渠道的效果，优化广告投放等营销策略。
 
 KeychainAccess
 des：提供了一种简单且安全的方式来与 iOS 系统的钥匙串（Keychain）进行交互。钥匙串是 iOS 用于存储敏感信息（如用户登录凭证，像用户名、密码，或者应用的私密配置信息等）的安全存储区域。使用 KeychainAccess 可以方便地在钥匙串中进行数据的存储、读取、更新和删除操作，确保这些敏感数据不会轻易被其他应用获取，保障用户数据安全。

 
 SwiftMessage
 des：用于在 iOS 应用中方便地显示各种样式的提示消息框，可自定义消息的内容、样式（如背景色、文本颜色、字体等）、显示位置（顶部、中部、底部等）以及显示时长等，为用户提供清晰直观的操作反馈，比如显示操作成功、失败或者警告等提示信息。
 
